public class Ejercicio1 {
    public static void main(String[] args) {
        /* Crea un programa que, dados unos valores fijos de altura y anchura, calcule
        el área de un rectángulo y lo muestre por pantalla.
        Pista: a = b x h
        */

        float b = 10.75f; // base rectángulo
        float h = 8.75f; // altura rectángulo
        float resultado = 0;

        resultado= b*h; // calculo area rectángulo (base * altura)

        System.out.println(resultado);

    }
}
